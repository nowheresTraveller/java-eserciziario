package org.example.mioeserciziario.motorino;

public class Padre {
    private int primoAttr;
    private int secondAttr;

    public Padre(int primoAttr, int secondAttr) {
        this.primoAttr = primoAttr;
        this.secondAttr = secondAttr;
    }

    public int getPrimoAttr() {
        return primoAttr;
    }

    public int getSecondAttr() {
        return secondAttr;
    }
}
