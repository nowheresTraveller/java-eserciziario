package org.example.mioeserciziario.progression;

public class Bonus {
    private int data;
    private int cifra;

    public Bonus(int data, int cifra) {
        this.data = data;
        this.cifra = cifra;
    }

    public int getData() {
        return data;
    }

    public int getCifra() {
        return cifra;
    }
}
